package in.kanth.beans;

public class SupportBean {
	private String suid;
	private String job;
	public SupportBean(String suid, String job) {
		super();
		this.suid = suid;
		this.job = job;
	}
	public String getSuid() {
		return suid;
	}
	public String getJob() {
		return job;
	}
	
	
	
}
